package com.zkt.find.goods.entity;

/**find用户想要实体*/
public class FindWantUserEntity {
	
	private String id;
	/**发现ID*/
	private String find_id;
	/**想要用户ID*/
	private String user_id;
	/**想要的时间*/
	private String want_time;
    /**用户昵称*/
	private String nickname;
	/**用户头像url*/
	private String user_pic;
	/**用户个性化签名*/
	private String introduction;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFind_id() {
		return find_id;
	}

	public void setFind_id(String find_id) {
		this.find_id = find_id;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getWant_time() {
		return want_time;
	}

	public void setWant_time(String want_time) {
		this.want_time = want_time;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getUser_pic() {
		return user_pic;
	}

	public void setUser_pic(String user_pic) {
		this.user_pic = user_pic;
	}

	public String getIntroduction() {
		return introduction;
	}

	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	
	

}
