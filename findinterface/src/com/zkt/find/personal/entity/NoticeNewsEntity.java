package com.zkt.find.personal.entity;

public class NoticeNewsEntity {

	private String id;
	private String rec_user_id;
	private String send_user_id;
	private String type;
	private String status;
	private String tine;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRec_user_id() {
		return rec_user_id;
	}
	public void setRec_user_id(String rec_user_id) {
		this.rec_user_id = rec_user_id;
	}
	public String getSend_user_id() {
		return send_user_id;
	}
	public void setSend_user_id(String send_user_id) {
		this.send_user_id = send_user_id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTine() {
		return tine;
	}
	public void setTine(String tine) {
		this.tine = tine;
	}
}
